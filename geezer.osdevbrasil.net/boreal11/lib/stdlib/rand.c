unsigned g_seed;
/*****************************************************************************
*****************************************************************************/
unsigned rand(void)
{
	if(g_seed == 0)
		g_seed = 1;
	if((((g_seed << 3) ^ g_seed) & 0x80000000L) != 0)
		g_seed = (g_seed << 1) | 1;
	else
		g_seed <<= 1;
	return g_seed - 1;
}
