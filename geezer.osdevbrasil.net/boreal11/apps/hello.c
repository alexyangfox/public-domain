/*----------------------------------------------------------------------------
"HELLO" TASK

EXPORTS:
int main(void)
----------------------------------------------------------------------------*/
#include <stdio.h> /* stdout, setbuf(), printf() */
/*****************************************************************************
*****************************************************************************/
#define	WAIT	0xFFFFFL

static void wait(void)
{
	unsigned long wait;

	for(wait = WAIT; wait != 0; wait--)
		/* nothing */;
}
/*****************************************************************************
*****************************************************************************/
int main(void)
{
setbuf(stdout, NULL);
	printf("Hello from thread 1\n");
	wait();
	while(1)
	{
		printf("thread 1 ");
		wait();
	}
	return 0;
}
